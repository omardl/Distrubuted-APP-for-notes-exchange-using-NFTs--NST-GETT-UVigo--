  async onSubmit()  {
 

    const provider = new ethers.providers.Web3Provider(window.ethereum)
    await provider.send("eth_requestAccounts", []);
    const signer = provider.getSigner();
    const dappuntesContract = new ethers.Contract(this.CONTRACT_ADDRESS, this.contractABI, signer);
    
    this.noteForm.value.owner = await(signer.getAddress());

    this.crudService.AddNote(this.noteForm.value).subscribe((res_node) => {
        
         const ipfs_uri =JSON.stringify(res_node).replaceAll('"', '')

         try {
          const createTxn = dappuntesContract['mintNFT'](signer.getAddress(), ipfs_uri);
 
          console.log('Create transaction started...', createTxn.hash);
    
          createTxn.wait();
          console.log('Created dappunte!', createTxn.hash);
    
        } catch (err: any) {
          console.error(err.message);
        }  
        
      }, (err) => {
        console.log(err);
    });

    this.ngZone.run(() => this.router.navigateByUrl('/'))


  }
